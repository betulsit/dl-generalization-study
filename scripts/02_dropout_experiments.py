import torch
import torch.nn as nn
import torch.optim as optim
import numpy as np
import matplotlib.pyplot as plt
import time
import copy
from torchvision import datasets, transforms
from torch.utils.data import DataLoader, random_split

# Consistency Rules
torch.manual_seed(42)
np.random.seed(42)
device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
print(f"Using device: {device}")

# Data loading (54k/6k/10k Split)
transform = transforms.Compose([
    transforms.ToTensor(),
    transforms.Normalize((0.5,), (0.5,))
])

full_train_dataset = datasets.FashionMNIST(root='./data', train=True, download=True, transform=transform)
test_dataset = datasets.FashionMNIST(root='./data', train=False, download=True, transform=transform)

# Consistent Split
train_subset, val_subset = random_split(full_train_dataset, [54000, 6000])

train_loader = DataLoader(train_subset, batch_size=64, shuffle=True)
val_loader = DataLoader(val_subset, batch_size=64, shuffle=False)
test_loader = DataLoader(test_dataset, batch_size=64, shuffle=False)

# MLP Architecture with Dropout
class DropoutMLP(nn.Module):
    def __init__(self, dropout_p):
        super(DropoutMLP, self).__init__()
        self.fc1 = nn.Linear(784, 128)
        self.dropout1 = nn.Dropout(p=dropout_p)
        self.fc2 = nn.Linear(128, 64)
        self.dropout2 = nn.Dropout(p=dropout_p)
        self.fc3 = nn.Linear(64, 10)

    def forward(self, x):
        x = x.view(-1, 784)
        x = torch.relu(self.fc1(x))
        x = self.dropout1(x)
        x = torch.relu(self.fc2(x))
        x = self.dropout2(x)
        x = self.fc3(x)
        return x

def train_and_validate(model, train_loader, val_loader, test_loader, p_val, epochs=15, lr=0.001):
    criterion = nn.CrossEntropyLoss()
    optimizer = optim.Adam(model.parameters(), lr=lr)

    history = {'train_loss': [], 'val_loss': [], 'train_acc': [], 'val_acc': []}
    start_time = time.time()

    for epoch in range(epochs):
        model.train()
        t_loss, t_correct, t_total = 0, 0, 0
        for imgs, lbls in train_loader:
            imgs, lbls = imgs.to(device), lbls.to(device)
            optimizer.zero_grad()
            outputs = model(imgs)
            loss = criterion(outputs, lbls)
            loss.backward()
            optimizer.step()

            t_loss += loss.item()
            _, pred = torch.max(outputs, 1)
            t_total += lbls.size(0)
            t_correct += (pred == lbls).sum().item()

        # Validation phase
        model.eval()
        v_loss, v_correct, v_total = 0, 0, 0
        with torch.no_grad():
            for imgs, lbls in val_loader:
                imgs, lbls = imgs.to(device), lbls.to(device)
                outputs = model(imgs)
                v_loss += criterion(outputs, lbls).item()
                _, pred = torch.max(outputs, 1)
                v_total += lbls.size(0)
                v_correct += (pred == lbls).sum().item()

        # Store history
        train_epoch_loss = t_loss/len(train_loader)
        val_epoch_loss = v_loss/len(val_loader)
        train_epoch_acc = t_correct/t_total
        val_epoch_acc = v_correct/v_total

        history['train_loss'].append(train_epoch_loss)
        history['val_loss'].append(val_epoch_loss)
        history['train_acc'].append(train_epoch_acc)
        history['val_acc'].append(val_epoch_acc)

        # AHSEN-STYLE PRINTING
        print(f"P={p_val} | Epoch [{epoch+1}/{epochs}] | "
              f"Train Loss: {train_epoch_loss:.4f} | Train Acc: {train_epoch_acc:.4f} | "
              f"Val Loss: {val_epoch_loss:.4f} | Val Acc: {val_epoch_acc:.4f}")

    # Final Test
    model.eval()
    test_correct, test_total, test_loss = 0, 0, 0
    with torch.no_grad():
        for imgs, lbls in test_loader:
            imgs, lbls = imgs.to(device), lbls.to(device)
            outputs = model(imgs)
            test_loss += criterion(outputs, lbls).item()
            _, pred = torch.max(outputs, 1)
            test_total += lbls.size(0)
            test_correct += (pred == lbls).sum().item()

    return history, test_loss/len(test_loader), test_correct/test_total, time.time() - start_time

# Run experiments
dropout_rates = [0.0, 0.2, 0.5]
results_summary = {}
histories = {}

for p in dropout_rates:
    print("\n" + "="*60)
    print(f"Running experiment with dropout p = {p}")
    print("="*60)

    model = DropoutMLP(p).to(device)
    # Note: we pass 'p' to the function now just for the print statement
    hist, t_loss, t_acc, duration = train_and_validate(model, train_loader, val_loader, test_loader, p)

    results_summary[p] = {
        'test_loss': t_loss,
        'test_acc': t_acc,
        'time': duration,
        'gap': hist['train_acc'][-1] - hist['val_acc'][-1]
    }
    histories[p] = hist

# 1. Plot Validation Accuracies
plt.figure(figsize=(15, 5))
plt.subplot(1, 3, 1)
for p in dropout_rates:
    plt.plot(histories[p]['val_acc'], label=f'p={p}')
plt.title('Validation Accuracy')
plt.legend()

# 2. Generalization Gap Plot
plt.subplot(1, 3, 2)
plt.plot(histories[0.0]['train_acc'], 'r--', label='Train (No Dropout)')
plt.plot(histories[0.0]['val_acc'], 'r', label='Val (No Dropout)')
plt.plot(histories[0.5]['train_acc'], 'g--', label='Train (p=0.5)')
plt.plot(histories[0.5]['val_acc'], 'g', label='Val (p=0.5)')
plt.title('The Generalization Gap')
plt.legend()

# 3. Validation Loss
plt.subplot(1, 3, 3)
for p in dropout_rates:
    plt.plot(histories[p]['val_loss'], label=f'p={p}')
plt.title('Validation Loss')
plt.legend()
plt.tight_layout()
plt.show()

# Final Comparison Table
print("\n" + "="*60)
print(f"{'Dropout P':<12} {'Test Acc':<12} {'Test Loss':<12} {'Gen. Gap':<12}")
print("-" * 60)
for p in dropout_rates:
    res = results_summary[p]
    print(f"{p:<12} {res['test_acc']:<12.4f} {res['test_loss']:<12.4f} {res['gap']:<12.4f}")