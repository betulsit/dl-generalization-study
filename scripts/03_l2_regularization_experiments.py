# Imports & Setup
import torch
import torch.nn as nn
import torch.optim as optim
import numpy as np
import matplotlib.pyplot as plt
import time

from torchvision import datasets, transforms
from torch.utils.data import DataLoader, random_split

# Reproducibility
torch.manual_seed(42)
np.random.seed(42)

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
print(f"Using device: {device}")

# Data Loading
transform = transforms.Compose([
    transforms.ToTensor(),
    transforms.Normalize((0.5,), (0.5,))
])

full_train_dataset = datasets.FashionMNIST(root='./data', train=True, download=True, transform=transform)
test_dataset = datasets.FashionMNIST(root='./data', train=False, download=True, transform=transform)

# SAME SPLIT AS TEAM
train_subset, val_subset = random_split(full_train_dataset, [54000, 6000])

train_loader = DataLoader(train_subset, batch_size=64, shuffle=True)
val_loader = DataLoader(val_subset, batch_size=64, shuffle=False)
test_loader = DataLoader(test_dataset, batch_size=64, shuffle=False)

print("Data ready.")

# Baseline MLP
class BaselineMLP(nn.Module):
    def __init__(self):
        super(BaselineMLP, self).__init__()
        self.net = nn.Sequential(
            nn.Flatten(),
            nn.Linear(784, 128),
            nn.ReLU(),
            nn.Linear(128, 64),
            nn.ReLU(),
            nn.Linear(64, 10)
        )

    def forward(self, x):
        return self.net(x)

# Training Function
def train_and_validate(weight_decay_value, epochs=15, lr=0.001):

    model = BaselineMLP().to(device)
    criterion = nn.CrossEntropyLoss()

    optimizer = optim.Adam(model.parameters(), lr=lr, weight_decay=weight_decay_value)

    history = {
        'train_loss': [],
        'val_loss': [],
        'train_acc': [],
        'val_acc': []
    }

    start_time = time.time()

    for epoch in range(epochs):
        # TRAIN
        model.train()
        t_loss, t_correct, t_total = 0, 0, 0

        for imgs, lbls in train_loader:
            imgs, lbls = imgs.to(device), lbls.to(device)

            optimizer.zero_grad()
            outputs = model(imgs)
            loss = criterion(outputs, lbls)
            loss.backward()
            optimizer.step()

            t_loss += loss.item()
            _, pred = torch.max(outputs, 1)
            t_total += lbls.size(0)
            t_correct += (pred == lbls).sum().item()

        # VALIDATION
        model.eval()
        v_loss, v_correct, v_total = 0, 0, 0

        with torch.no_grad():
            for imgs, lbls in val_loader:
                imgs, lbls = imgs.to(device), lbls.to(device)

                outputs = model(imgs)
                loss = criterion(outputs, lbls)

                v_loss += loss.item()
                _, pred = torch.max(outputs, 1)
                v_total += lbls.size(0)
                v_correct += (pred == lbls).sum().item()

        train_loss = t_loss / len(train_loader)
        val_loss = v_loss / len(val_loader)
        train_acc = t_correct / t_total
        val_acc = v_correct / v_total

        history['train_loss'].append(train_loss)
        history['val_loss'].append(val_loss)
        history['train_acc'].append(train_acc)
        history['val_acc'].append(val_acc)

        print(f"WD={weight_decay_value} | Epoch [{epoch+1}/{epochs}] | "
              f"Train Loss: {train_loss:.4f} | Train Acc: {train_acc:.4f} | "
              f"Val Loss: {val_loss:.4f} | Val Acc: {val_acc:.4f}")

    # TEST
    model.eval()
    test_loss, correct, total = 0, 0, 0

    with torch.no_grad():
        for imgs, lbls in test_loader:
            imgs, lbls = imgs.to(device), lbls.to(device)

            outputs = model(imgs)
            loss = criterion(outputs, lbls)

            test_loss += loss.item()
            _, pred = torch.max(outputs, 1)
            total += lbls.size(0)
            correct += (pred == lbls).sum().item()

    test_acc = correct / total

    return history, test_loss / len(test_loader), test_acc, time.time() - start_time

# Run Experiments
weight_decay_values = [0.0, 1e-4, 1e-3]

results = {}
histories = {}

for wd in weight_decay_values:
    print("\n" + "="*60)
    print(f"Running experiment with weight_decay = {wd}")
    print("="*60)

    hist, t_loss, t_acc, duration = train_and_validate(wd)

    results[wd] = {
        'test_loss': t_loss,
        'test_acc': t_acc,
        'time': duration,
        'gap': hist['train_acc'][-1] - hist['val_acc'][-1]
    }

    histories[wd] = hist

# Plots
plt.figure(figsize=(15,5))

# 1. Validation Accuracy
plt.subplot(1,3,1)
for wd in weight_decay_values:
    plt.plot(histories[wd]['val_acc'], label=f'wd={wd}')
plt.title("Validation Accuracy")
plt.legend()

# 2. Generalization Gap
plt.subplot(1,3,2)
plt.plot(histories[0.0]['train_acc'], 'r--', label='Train (No L2)')
plt.plot(histories[0.0]['val_acc'], 'r', label='Val (No L2)')
plt.plot(histories[1e-3]['train_acc'], 'g--', label='Train (wd=1e-3)')
plt.plot(histories[1e-3]['val_acc'], 'g', label='Val (wd=1e-3)')
plt.title("Generalization Gap")
plt.legend()

# 3. Validation Loss
plt.subplot(1,3,3)
for wd in weight_decay_values:
    plt.plot(histories[wd]['val_loss'], label=f'wd={wd}')
plt.title("Validation Loss")
plt.legend()

plt.tight_layout()
plt.show()

# Final Table
print("\n" + "="*60)
print(f"{'Weight Decay':<15}{'Test Acc':<15}{'Test Loss':<15}{'Gen Gap':<15}")
print("-"*60)

for wd in weight_decay_values:
    res = results[wd]
    print(f"{wd:<15}{res['test_acc']:<15.4f}{res['test_loss']:<15.4f}{res['gap']:<15.4f}")